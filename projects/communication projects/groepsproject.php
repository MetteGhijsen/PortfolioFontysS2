            <h1>Afspraken</h1>
           <p>Als groep zijn wij nooit echt streng geweest naar elkaar. Je maakt je werk, je laat om de zoveel tijd even zien wat je maakt, en je zorgt dat je gewoon aanwezig bent. Als je niet aanwezig kan zijn om watvoor reden dan ook, dan moet je dat even melden van tevoren. Dit mocht dan via Whatsapp of Teams. Hoelaat je dit doet, daar hadden we geen afspraken over gemaakt.
               <br></br>
               Verder merkten we wel dat we ons op school niet altijd voor 100% konden inzetten. Dit kwam vaak doordat we afgeleid raakten door bijvoorbeeld elkaar, of door omgevingsgeluiden in de OIL. Daarom spraken we soms de avond voor een lesdag met elkaar af om de volgende dag thuis te blijven.
               <br></br>
               Ik merkte dat ik binnen de groep altijd best wel functioneerde als groepsleider. Naast dat ik de persoon was die het vaakst contact had met onze opdrachtgever, was ik ook de persoon die het meeste deed. Wanneer we een uitgebreid groepsproject hadden, was ik de persoon die alle onderdelen van iedereen bij elkaar zette in één groot bestand. Ook wilde ik graag nog even de laatste kwaliteitscheck doen; zorgen dat alle spelfouten eruit waren e.d. Daarnaast was ik ook de persoon die iedereen er door het semester heen aan herinnerde dat er bepaalde deadlines aankwamen.
               Kortom, ik heb best een significant bijdrage geleverd aan het uiteindelijke resultaat van ons groepsproject. Dat kan allemaal natuurlijk niet zonder goede communicatie. Ik ben erg tevreden met hoe ik dingen heb aangepakt, en ik denk dat mijn professionele houding is gegroeid.
           </p>
            <h1>Planning</h1>
            <p>De planning is opgesteld in onze zelfgemaakte Mediaverkenning. We werken met de SCRUM-methode, wat betekent dat al het te maken werk wordt opgedeeld in sprints. Elke sprint duurde 3 weken. Met stand-ups op het begin en/of het einde van de dag hielden we elkaar op de hoogte van onze individuele werkzaamheden. Ook bespraken we dit regelmatig gedurende de dag zelf.
                Wanneer we bestanden met elkaar deelden gebruikten we daar onze gezamelijke OneDrive voor. Zo was alles overzichtelijk en makkelijk te vinden.
            </p>
