            <h1>Proces</h1>
            <p>Voor deze opdracht had ik eigenlijk liever Indesign willen gebruiken, aangezien dat programma echt precies gemaakt is voor hetgeen dat ik wil maken. Photoshop is echt voor bewerking van foto’s en met Indesign kun je de layout van boekjes en magazines weergeven. Echter was mijn ervaring met Indesign nog te beperkt om hier een heel project in te maken. Daarom heb ik besloten Photoshop te gebruiken.
                <br><br>
                Ik wilde voor het brandbook graag de huiskleuren gebruiken van Coffeelab zodat het boek en de inhoud een mooi geheel vormde. Bram Tuns had mij bij de feedback over mijn website de tip gegeven om wat meer witruimtes te gebruiken, om een minimalistischer geheel te creeëren. Dit komt rustiger over voor de gebruiker, of in mijn geval, de lezer. Ik vond dit goede feedback, en ik heb me hier gedurende het design van het hele brandbook op proberen te focussen. In het brandbook zijn dan ook hier en daar wat witruimtes of lege stukken terug te zien.
                <br><br>
                Het leek me leuk om op de front- en backcover van het brandbook hetzelfde patroon als in het logo te gebruiken. Zo is ook echt te zien dat het boekje bij Coffeelab hoort. Ik vind dit een mooie toevoeging, waar ik erg tevreden over ben. Daarnaast heb ik ook nog binnenin het boekje hier en daar wat groene elementen proberen toe te voegen. Op deze manier hoopte ik alle kleuren een beetje met elkaar te balanceren, hoewel off-white natuurlijk het vaakst voorkomt.
                <br><br>
                Ik heb niet echt iteraties gemaakt na mijn eerste ontwerp, omdat Evert aangaf tevreden te zijn met wat ik op dit moment had. Daarnaast was ik zelf ook best wel tevreden over mijn ontwerp.
            </p>
