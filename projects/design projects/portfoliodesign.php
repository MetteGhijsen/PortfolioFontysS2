                week al een site staande moest hebben. Dus ik had ook niet het gevoel dat ik mijn tijd kon nemen met het
                designproces. Daarom besloot ik voor mijn eerste portfoliowebsite aan de slag te gaan met een iets
                simpeler ontwerp dat ook te realiseren was. Ik heb het hele ontwerp weggegooid en ben opnieuw begonnen.
