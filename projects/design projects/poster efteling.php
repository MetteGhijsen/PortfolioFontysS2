            <p> Normaal gesproken zijn de vereisten voor een Salve Mundi poster wat uitgebreider, er moet meestal een begin- en eindtijd op de poster te vinden zijn. Ook staat er meestal een locatie op de poster. Omdat de Efteling een vaste openings- en sluitingstijd heeft, was dat voor deze poster niet nodig. Daarnaast was de locatie van de activiteit al redelijk af te leiden uit de naam, dus ook dit was niet nodig.
                <br></br>
