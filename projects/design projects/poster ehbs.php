            <h1>Inleiding</h1>
            <p> Omdat ik lid ben van de mediacommissie binnen Salve Mundi, kreeg ik van de studiecommissie de opdracht
                om een poster te maken om hun bijlessen te promoten. Het doel van de poster was om duidelijk te maken
                aan de studenten in het startsemester waar en hoelaat ze ergens moesten zijn om de bijles te volgen.
                Daarnaast moest er ook een QR code op de poster zodat studenten de EHBS Teams omgeving te joinen.</p>
        </div>
        <div class="afbeelding">
            <img id="afbeelding1" src="../../img/design/posterstudiecommissiev3.png" projectimage">
        </div>
        <div class="text2">
            <h1>Ervaring</h1>
            <p> Als eerste ben ik aan de slag gegaan, en heb ik een custom QR code aangemaakt met de Teams link erin. Ik
                heb meerdere mensen laten testen of deze werkt, en dat deed het gelukkig. Daarna ben ik gaan nadenken
                over het design. Salve Mundi heeft een stijlcode. We hebben bepaalde kleuren en lettertypen die we
                moeten gebruiken voor de poster. Echter ben ik zelf niet erg veel fan van het lettertype dat wij moeten
                gebruiken van Salve Mundi, dus ik heb gevraagd of ik zelf een lettertype mocht kiezen, en dat mocht! Ik
                heb besloten voor het lettertype “Montserrat Bold” te kiezen. Dit is een lettertype dat ik zelf vaker
                heb gebruikt, en ik vind het een mooi en duidelijk lettertype dat goed is voor titels. Voor de kleinere
                subteksten heb ik “Montserrat Light” gebruikt.
                Echter waren er wel wat huisstijlregels waar ik niet vanaf mocht wijken. Zoals het logo dat op de poster
                voor moest komen, en de “Salve Mundi paars”. Anders weten mensen natuurlijk niet dat de bijlessen
                verzorgd worden door onze studievereniging. Om ervoor te zorgen dat de poster een beetje een geheel
                vormt, heb ik besloten om het logo linksboven wit te maken, net als de tekst. Ik heb gekozen om het
                paars in de poster te verwerken door subtiele rechthoeken achter de tekst en achter de QR-code te
                plaatsen en daardoor een soort “schaduw-effect” te creeëren. Ik merkte ook dat de tekst en de QR-code
                hierdoor beter opvielen. Kortom, ik was zelf erg tevreden.
                <br></br>
                Omdat dit de eerste poster is die ik heb gemaakt in een stijl zoals deze, wist ik niet zo goed of ik nog een tweede versie moest maken. Ik had besloten dit niet te doen, omdat de opdrachtgever al aangegeven had tevreden te zijn met de eerste versie.

            </p>
        </div>
        <div class="text4">
            <h1>Wat heb ik geleerd?</h1>
            <p>Ik heb geleerd dat ik een poster misschien moet testprinten, om dan eventueel het design nog te kunnen
                aanpassen naar hoe de kleuren uitvallen. Op beeldschermen zien kleuren er toch altijd anders uit dan in
                het echt, en er is niks mis met iets uitprinten om het even te testen.</p>
        </div>
        <div class="text5">
            <h1>Feedback</h1>
            <p>Ik heb feedback gevraagd aan een aantal mensen, beginnend met mijn opdrachtgever natuurlijk. De poster
                was in de groepsapp gestuurd van de studiecommissie, en iedereen was positief verrast door de poster. Ik
                heb veel complimenten mogen ontvangen, en ik ben erg blij dat iedereen tevreden is met mijn design.
                Echter kreeg ik later wel nog te horen dat bij het printen van de poster deze erg donker uitviel. In het
                vervolg moet ik hier misschien op letten als ik een poster ontwerp. Ik denk dat ik dit zou kunnen
                voorkomen door de poster een keer te testprinten, zodat ik weet hoe de kleuren uitvallen. Ondanks het
                feit dat de poster donker uitviel, was alles wel nog goed leesbaar doordat ik witte teksten had
                gebruikt. Ook de QR-code werkte nog prima, en was nog gewoon scanbaar.
                Behalve dat ik feedback had gevraagd aan de studiecommissie, heb ik ook feedback gevraagd aan de
                studenten van het startsemester, voor wie de poster bedoeld is. Ook zij gaven aan dat de poster mooi en
                overzichtelijk was. Ze wisten precies waar ze op welk tijdstip moesten zijn, en wat ze moesten doen om
                in de Teams omgeving te komen.
            </p>
