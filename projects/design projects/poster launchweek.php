            ze het juist interessant hoe ik het heb aangepakt. Ik denk ook dat ik de volgende keer beter in Illustrator kan werken,
            en van de poster een SVG moet maken in plaats van een PNG. Hoewel ik hier geen feedback over heb gekregen, verwacht
            ik wel dat docenten hier blijer mee zullen zijn in de toekomst. Ik moet echter wel nog leren hoe Illustrator werkt.</p>
