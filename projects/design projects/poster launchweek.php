            <p> Toen ik eenmaal mijn poster bestand in Photoshop aanmaakte heb ik echt een tijdje naar de lege pagina
                gestaard. Ik wist totaal niet wat ik wilde. Op een gegeven moment ben ik toen begonnen met het maken van
                wat schetsen met mijn tekentablet. Ik zocht een coole manier om mijn naam te branden. Het leek me enorm
                vet om zelf een lettertype te maken, en aangezien ik een hele week de tijd had voor dit project leek me
                dat ook wel haalbaar. Na een beetje te hebben nagedacht over hoe ik mijn naam wilde branden, stuitte ik
                per toeval op het lettertype “Scream Real” van de film “Scream”. Omdat de M van dit lettertype verder
                naar beneden gaat, en mijn naam begint met een M, realiseerde ik me dat dit het perfecte lettertype was
                om mijn naam mee te branden. Toen ik eenmaal had besloten dat ik dit ging gebruiken, kwam de rest van de
                poster een beetje vanzelf. Als eerste heb ik de 2039 toegevoegd. Het is nu 2021, en wanneer ik twee keer
                zo oud ben als nu, is het 2039. Om het jaartal een beetje te scheiden van de rest van de tekst, had ik
                besloten er een subtiel streepje onder te zetten. Dit zag er mooi uit, en hier was ik tevreden mee. De
                rest van de lijnen op de poster zijn allemaal met de hand getekend met mijn tekentablet, en de teksten
                onderin de poster heb ik aan het einde nog toegevoegd zodat het echt op een filmposter leek. Ik besloot
                ook nog bij de acteurs wat vrienden van mij neer te zetten, omdat ze hierom hadden gevraagd.
                Ik ben erg tevreden met de poster, en ik denk toch dat mijn persoonlijkheid hier goed in naar voren
                komt. Ik weet namelijk niet zo goed wat ik aan het doen ben, of wat ik nou eigenlijk wil doen. Maar
                zolang ik dingen kan maken waar andere mensen blij van worden, ben ik ook blij, en dat is wat ik een
                beetje in deze poster naar voren wilde laten komen.
            </p>
        </div>
