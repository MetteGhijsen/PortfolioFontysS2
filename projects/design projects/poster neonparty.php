            <h1>Ervaring</h1>
            <p> Omdat het om een neon party ging wilde ik wel graag dat het thema neon duidelijk naar voren kwam in de poster. Ik besloot even wat inspiratie op te doen en te kijken naar hoe andere mensen dit hebben aangepakt. Ik stuitte al gauw op de poster voor het glow in the dark feest van een andere studievereniging van Fontys, genaamd FACT. Het leek mij een goed idee om deze als inspiratie te gebruiken.
                <br><br>
                Als eerste ben ik op zoek gegaan naar duidelijke lettertypen die ik kon gaan gebruiken voor het neon-effect dat ik probeerde te bereiken met mijn letters. Ik zocht naar een duidelijk recht letterype, dat ik in grote hoofdletters kon gebruiken voor het woord neon. Daarna ben ik naar een wat slordiger schrijflettertype gaan zoeken voor het woord party. Toen ik beide lettertypen gevonden had, heb ik deze woorden op zo’n manier onder elkaar gezet dat ze netjes in het midden stonden.
                Het was nu tijd om het glow effect te gaan maken. Ik heb gekozen voor blauw en roze, omdat dit vrij opvallende kleuren zijn die een mooi contrast met elkaar vormen. Het glow effect heb ik gemaakt door bij de tekstlaag “blending options” te selecteren, en dan “outer glow” aan te klikken. Daarna heb ik een kleur en dikte gekozen. Als laatste heb ik Montserrat Bold en Montserrat Regular gebruikt om de belangrijke informatie bovenaan weer te geven.
            </p>
        </div>
        <div class="text4">
            <h1>Wat heb ik geleerd?</h1>
            <p>Ik heb geleerd hoe ik een glow achter mijn tekst neerzet. Daarnaast heb ik ook wel iets geleerd over de positionering van titels en informatie. Eerder was ik bij posters altijd geneigd de informatie iets meer in het midden of aan de zijkant van de pagina neer te zetten. Echter denk ik dat de titel en het thema van het evenement beter tot zijn recht komt als je wat ruimte over laat en de informatie wat minimaler weergeeft.</p>
