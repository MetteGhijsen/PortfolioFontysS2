            <b>Fullpage.js</b>
            <br>
            Fullpage.js helpt je met het maken van fullscreen scroll websites. Wanneer je de library gebruikt is
            alles ook meteen responsive, zodat het ook werkt op telefoons of tablets.
            <br>
            <a href="https://alvarotrigo.com/fullPage/#page1">Meer informatie →</a>
            <br></br>
            <b>Moment.js</b>
            <br>
            Moment.js maakt het je makkelijker om te werken met tijden en datums. De library helpt je door
            bijvoorbeeld het manipuleren, formatteren, valideren of organiseren van tijden of datums.
            <br>
            <a href="https://momentjs.com/">Meer informatie →</a>
            <br></br>
            <b>Cleave.js</b>
            <br>
            Dit is een library voor die super handig is wanneer je een contact-formulier op je website hebt staan.
            Het kan attributen zoals telefoonnummers, creditcardgegevens, datums etc. herschrijven in het juiste
            formaat.
            <br>
            <a href="https://nosir.github.io/cleave.js/">Meer informatie →</a>
            <br></br>
            <b>React.js</b>
            <br>
            React is een library die kan worden gebruikt voor het makkelijk bouwen van user-interfaces. Je kunt
            verschillende componenten bouwen die je vaker laat terugkomen op je website, zoals knoppen, titels,
            timers of textboxen.
            <br>
            <a href="https://reactjs.org/">Meer informatie →</a>
            <br></br>
            <b>Hammer.js</b>
            <br>
            Hammer.js kan worden gebruikt om multi-touch gebaren toe te voegen aan je webpagina. Het is een simpele
            library die je pagina snel wat speelser en interactiver kan maken.
            <br>
            <a href="https://hammerjs.github.io/">Meer informatie →</a>
