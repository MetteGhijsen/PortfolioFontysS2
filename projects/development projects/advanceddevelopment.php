            <h1>Week 1</h1>
            <p><b>Anime.js</b>
                <br>
                Dit is een library die kan worden gebruikt om animaties toe te voegen aan je website.
                Anime.js werkt met CSS, SVG’s, DOM attributes en Javascript objecten. Geschikt voor bijna ieder project
                dus!
