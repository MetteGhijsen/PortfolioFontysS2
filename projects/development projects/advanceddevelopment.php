                <b>Aos.js</b>
                <br>
                De AOS, oftewel de Animate On Scroll library die kan worden gebruikt om animaties uit te voeren wanneer
                er gescrolld wordt. Zo hoef je nooit meer zelf je animaties te schrijven.
                <br>
                <a href="https://michalsnik.github.io/aos/">Meer informatie →</a>
                <br></br>
                <b>Slick</b>
                <br>
                Slick is een library die kan worden gebruikt voor het maken van sliders of carousels. Deze zijn ook
                meteen responsive, zodat ze gemakkelijk op andere apparaten te bekijken zijn.
                <br>
                <a href="https://kenwheeler.github.io/slick/">Meer informatie →</a>
                <br></br>
                <b>Parsley.js</b>
                <br>
                Parsley is een Javascript library die goed samenwerkt met contactformulieren op een website. Met deze
                library kun je bepaalde velden verplicht maken, en ervoor zorgen dat gebruikers het formulier niet
                kunnen verzenden tenzij ze alles goed hebben ingevuld.
                <br>
                <a href="http://parsleyjs.org/ ">Meer informatie →</a>
                <br></br>
                <b>Granim.js</b>
                <br>
                Met Granim kun je gemakkelijk (geanimeerde) gradients toevoegen aan je website. Ze kunnen worden
                gebruikt als achtergrond, of als object, maar ook als image overlay of image mask.
                <br>
                <a href="https://sarcadass.github.io/granim.js/">Meer informatie →</a>
