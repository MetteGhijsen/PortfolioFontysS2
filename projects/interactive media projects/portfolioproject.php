            <h1>Wat heb ik geleerd?</h1>
            <p>Dit is de eerste website die ik ooit heb gemaakt, dus het was een behoorlijk leerproces. Er zijn veel dingen die ik nu anders zou doen. Maar door mijn gebrek aan kennis in HTML/CSS/Javascript aan het begin van het semester heb ik sommige dingen redelijk onhandig aangepakt. Wat HTML en CSS betreft heb ik veel geleerd over flexboxen en grids. Ik heb ook geleerd hoe ik een gebruiksvriendelijke layout maak, en hoe ik feedback van verschillende mensen kan verwerken zodat de website optimaal werkt voor iedereen. Ik heb ook geleerd dat je bij je eigen waarden moet blijven, en niet altijd naar iedereens feedback hoeft te luisteren. Ik denk dat het belangrijk is om hier een mooi balans tussen te vinden.</p>
