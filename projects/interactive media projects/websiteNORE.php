            <h1>Gebruikerstesten</h1>
            <p><b>Medestudent 1</b><br>
                We hebben een aantal medestudenten naar onze website laten kijken. Hoewel de feedback erg positief was, hadden sommige studenten wel wat aanmerkingen. Op onze homepagina was een fret met een ballonnetje te zien. Sommige studenten vonden deze afleidend, of wisten niet wat deze deed op de pagina. Om de website wat minder statisch te maken kwam een medestudent met het idee om deze fret rond te laten vliegen elke 20 seconden. De rest van de tijd is deze uit beeld zodat het geen al te grote afleiding vormt. Dit was al een enorme verbetering volgens een aantal studenten.
                <br><br>

                <b>Medestudent 2</b><br>
                Onze opdrachtgever wilde erg graag een soort rad van fortuin op de kunstpagina. Wanneer je aan dit rad draaide kwam er een willekeurig stukje kunst van haar tevoorschijn. We hebben haar dit rad laten tekenen en het geïmplementeerd in de site. Echter kwamen veel studenten met de opmerking dat ze niet begrepen waar dit rad voor bedoeld was. Daarnaast zag het er volgens velen erg chaotisch uit op de al drukke kunstpagina. Daarom hebben we besloten in overleg met Tessa het kunstrad te veranderen in een knop, om zo wat rust in het hoofd van de gebruiker te creeëren. We vinden dit zelf een enorme verbetering.
                <br><br>

                <b>Leeftijdsgroep 50+</b><br>
                We hebben de website neergelegd bij een leeftijdsgroep van 50+ om te kijken of de layout te begrijpen was. Hier kwam uit dat het verschil tussen de pagina voor de concertfoto’s en de pagina voor de epkfoto’s niet helemaal duidelijk was. Daarom hebben we besloten de naam van de concertfoto-pagina te veranderen naar “gallery” in plaats van “photo’s”. Op deze manier hoopte we meer duidelijkheid te creeëren voor de gebruiker.
                <br><br>

                <b>Vriend zonder grafische achtergrond</b><br>
                We hebben ook nog feedback gevraagd aan een van onze gezamelijke vrienden. Deze probeerde de website op zijn telefoon te bekijken, en kwam tot de conclusie dat dit lastig ging. Hoewel onze website in zekere zin enigszins responsive was, had hij toch nog een aantal opmerkingen. Een aantal afbeeldingen zaten namelijk in de weg, de ruimteverdeling was niet juist en teksten overlapte op een aantal plekken. Om dit op te lossen hebben we extra afbeeldingen uitgezet op mobiele schermen, ruimteverdelingen opnieuw ingesteld en teksthoogtes verbeterd.
