            <p>De eerste specialisatiemodule die ik heb gevolgd voor de leeruitkomst professional identity is Branding. Tijdens deze specialisatie was het de opdracht om onderzoek te doen naar een bepaald merk. We kregen de keuze uit vijf verschillende merken. Van deze merken heb ik voor Coffeelab gekozen. Coffeelab is een koffiezaakje te vinden in grote steden verspreid door Brabant.
                <br><br>
                Voor mijn onderzoek heb ik gebruik gemaakt van “The Brand Canvas” onderzoeksmethode. Deze methode wordt vaak gebruikt door beginnende ondernemers om succesvol een merk op te zetten. The Brand Canvas is eigenlijk een soort tabel die je invult waarin je onderzoek doet naar de persoonlijkheid van je merk. Denk hierbij aan de eigenschappen van het merk, wat je probeert over te brengen, een positioneringsverklaring en een kleurenpalet/typografie. <br>
                Hier kun je meer over vinden bij <a href="../research%20projects/brandingresearch.php" style="color: #0900b6"><b>research</b></a>.
                <br><br>
                Voor het maken van het brandbook heb ik gebruik gemaakt van Adobe Photoshop. Hierbij heb ik erop gelet dat het uiterlijk van het boek zoveel mogelijk matcht met de branding van Coffeelab. Denk hierbij aan de kleuren, lettertypen en vormen die ik heb gebruikt. Meer hierover kun je vinden bij
                <a href="../design%20projects/brandingcoffeelab.php" style="color: #0900b6"><b>design</b></a>.
            </p>
