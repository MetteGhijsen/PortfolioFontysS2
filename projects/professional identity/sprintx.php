            <h1>Inleiding</h1>
            <p>
                Na onze groepsopdracht volgde er een extra sprint waarbij je je persoonlijke leerdoelen kon kiezen. Ik had interesse in het maken van animaties en besloot mij hierin te verdiepen.
            </p>
            <h1>Week 1</h1>
            <p>
                Nadat ik goedkeuring van Bram had gekregen voor het maken van een animatie, ging ik kijken naar welke software ik het beste kon gebruiken. Ik kwam tot de conclusie dat ik graag gebruik wilde maken van Adobe Animate. Ik had al de complete Creative Cloud, en ik wist van een aantal animators dat ze deze software ook gebruikten. <br><br>

                Mijn eerste week bestond vooral uit het bekend worden met Adobe Animate. Ik heb een paar basistutorials gevolgd over de werking van het programma. Een aantal van Animate zelf, maar ook een aantal van YouTube. Ik merkte dat ik de tutorials van YouTube veel fijner vond, omdat ik daar ook daadwerkelijk zelf aan het tekenen was, in plaats van dat ik vooraf gemaakte assets gebruikte van Adobe. Nadat ik bekend was met de werking van het programma, besloot ik nog wat onderzoek te doen naar animatie. Hoe animeer je iemand die loopt? Hoe animeer je zwaartekracht? Hoe animeer je water? Hoe werkt union skinning? Daarnaast heb ik ook nog onderzoek gedaan naar framerate e.d. <br>
                Voor mijn eindanimatie heb ik besloten “On twos” (elke twee frames) te animeren, in 24 frames per seconde. Echter kwam ik er soms achter dat ik voor snelle bewegingen beter elke frame kon animeren. Dit gaf ook een vloeierende animatie.
                <br><br>
            </p>
            <h1>Week 2</h1>
            <p>
                In de tweede week ben ik aan de slag gegaan met mijn concept. Ik heb nagedacht wat ik precies wilde, en hoe ik dit ging vormgeven. Ik heb wat schetsen van mijn ideeën gemaakt. Dit was best wel een gedoe, omdat ik telkens weer nieuwe ideeën kreeg. Ik vond het ook erg lastig, omdat mijn ervaring in animeren erg beperkt was. Ik wist niet wat ik moest verwachten, en wat precies haalbaar was. Ik wist wel dat ik minimaal 12 (24/2) frames per seconde moest animeren. Mijn eindanimatie was rond de 15 seconden.
            </p>
            <h1> Week 3 </h1>
            In de derde week was het tijd om mijn animatie te realiseren. Ik ging aan de slag in Adobe Animate, met de kennis die ik in week 1 had opgedaan. Ik begon met het maken van schetsen, waarna ik de line-art heb gemaakt. Daarna heb ik de animatie ingekleurd.
        </div>

        <div class="text3">
            <h1>Wat heb ik geleerd?</h1>
            <p>Ik heb best wel veel geleerd van deze sprint. Als eerste heb ik geleerd hoe ik moet werken met Adobe Animate. Uiteraard beheers ik geen hoog niveau van Animate, maar wanneer ik in de toekomst verder wil gaan, of simpele animaties wil maken voor op websites is dit wel een mooie basis om op verder te bouwen. Daarnaast heb ik wat kennis opgedaan over animeren. Specifiek weet ik nu hoe het werkt met animeren in frames. Wat is de aanbevolen framerate, en op watvoor manieren kun je de frames invullen? Ook heb ik geleerd hoe je frame bij frame animaties tekent. Je kan bijvoorbeeld animeren door afbeeldingen te vervormen, maar ik heb gebruik gemaakt van mijn tekentablet om zo elke frame zelf te tekenen. </p>
        <h1>Wat zou ik anders doen?</h1>
            <p>Met de kennis die ik heb opgedaan zijn er wel dingen die ik anders zou doen. Ik ben namelijk toch niet helemaal tevreden met de kwaliteit van mijn eindanimatie. Ik zou de kwaliteit kunnen verbeteren door een iets rustiger en consistenter kleurenpalet aan te houden. De video is best wel fel nu. Daarnaast zou ondertiteling of een diepgaander script de kwaliteit van de video ook wel goed doen. Als laatste zou ik on ones animeren in plaats van on twos. Ik heb nu alleen on twos geanimeerd door een gebrek aan tijd. Maar denk dat de animatie er een stuk beter en vloeiender uit zou zien als ik gewoon on ones had geanimeerd.</p>
