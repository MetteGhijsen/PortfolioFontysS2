            <h1>Inleiding</h1>
            <p>
                Binnen ons project was ik verantwoordelijk voor de website van NORE. Ik mocht met Tessa gaan bepalen welke kleuren of welke lettertypen er gebruikt gingen worden voor de website. Ik heb hier uitgebreid onderzoek naar gedaan.
            </p>
            <h1>Gesprek Tessa</h1>
            <p>
                Als eerste heb ik een uitgebreid gesprek met Tessa gevoerd over welke kleuren ze op haar website wilde gebruiken. Ze gaf aan dat ze vaak kunst maakte met de kleuren rood, groen, blauw en paars. Ze stuurde ook nog tinten olijfgroen en pastelpaars die ze erg mooi vond. Met deze informatie ben ik aan de slag gegaan, en ik heb een afbeelding opgesteld met allerlei tinten kleuren. Ik heb haar hieruit een aantal kleuren laten kiezen.
                <br><br>
                Daarnaast heb ik ook nog een lijst met potentiële lettertypen gemaakt. Het logo van de band is heel rond en speels, dus ik wilde dat het lettertype dit gevoel ook overbracht. Echter ging Tessa’s voorkeur heel erg uit naar het lettertype dat er op dat moment werd gebruikt, “Geo”. Het lettertype was erg rond en vierkant, dit paste naar mijn mening totaal niet bij het gevoel dat de groep met hun band probeerde over te brengen. Gelukkig hebben ik en mijn projectgroep Tessa een beetje kunnen overtuigen.
            </p>
            <h1>Opstellen brandbook</h1>
            <p>Bij het opstellen van het brandbook heb ik wat onderzoek gedaan naar colortheory. De reden waarom rood en groen goed bij elkaar passen is omdat deze kleuren samen een heel goed contrast vormen. Als je kijkt naar het kleurenwiel, liggen deze twee kleuren precies tegenover elkaar. Net zoals bijvoorbeeld blauw en oranje. Twee kleuren die dicht bij elkaar liggen, zoals bijvoorbeeld paars en blauw, passen minder goed bij elkaar omdat deze niet contrasterend zijn naar elkaar. Om deze reden heb ik dus de kleuren rood en groen gekozen. Daarnaast heb ik ook paars gekozen. Echter kan paars dus wel alleen in combinatie met groen worden gebruikt voor een mooie look.
                <br><br>
                Voor de lettertypen ben ik uiteindelijk gegaan voor een combinatie tussen Montserrat en Poppins. Dit zijn allebei mooie, ronde, duidelijke lettertypen die goed aansluiten bij het gevoel dat de band over probeert te brengen.
            </p>
            <h1>Wat heb ik geleerd?</h1>
            <p>Ik heb geleerd dat je het niet altijd eens kan zijn met je opdrachtgever. Soms is er een meningsverschil en dan moet je je opdrachtgever advies geven. In dat advies moet je met duidelijke argumenten of onderzoeken komen die jouw statement ondersteunen. Dit kan erg lastig zijn, maar ik denk wel dat dit iets is waar ik in ben gegroeid.</p>
