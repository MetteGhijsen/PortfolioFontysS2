            <h1>Inleiding</h1>
            <p>
                Voor de specialisatie Branding moest ik onderzoek doen naar een bedrijf. Voor dit onderzoek heb ik gebruik gemaakt van “The Brand Canvas” (“Business Model Canvas” methode uit het “Stepping Stones” CMD Methods Pack onderdeel). Daarnaast heb ik voor het noteren van mijn bronnen gebruik gemaakt van de APA-stijl (American Psychological Assocation).
            </p>
            <h1>Ervaring</h1>
            <p>
                Als bedrijf om onderzoek naar te doen had ik gekozen voor Coffeelab. Dit bleek achteraf niet echt een handige keuze te zijn. Doordat Coffeelab een Nederlands bedrijf was, was er niet veel over te vinden online. Ik had beter een bedrijf zoals Netflix, of Goose Island kunnen kiezen. De weinige sites die ik heb kunnen vinden over Coffeelab, heb ik dan ook als bronnen gebruikt. Denk hierbij aan de bedrijfswebsite van Coffeelab, aan hun webshop en aan een website die recensies schrijft over gezellige koffieplekjes.
                <br><br>
                Voor mijn onderzoek heb ik gebruik gemaakt van “The Brand Canvas” onderzoeksmethode. Deze methode wordt vaak gebruikt door beginnende ondernemers om succesvol een merk op te zetten. The Brand Canvas is eigenlijk een soort tabel die je invult waarin je onderzoek doet naar de persoonlijkheid van je bedrijf/merk.
                <br><br>
                Al mijn bronnen heb ik genoteerd in APA-stijl. Dit houdt in dat ik bij citaten, of bij stukken informatie verwijs naar de bron die ik heb gebruikt. Dit doe ik door de naam en het publicatiejaar van de bron te noemen. Deze is vervolgens terug te vinden op mijn bronnenlijst.

            </p>
            <h1>Wat heb ik geleerd?</h1>
            <p>Ik heb geleerd wat “The Brand Canvas” is en hoe ik deze moet invullen. Als ik ooit een eigen bedrijf zou willen starten is dit een erg nuttige manier om de persoonlijkheid van mijn merk te ontdekken. Daarnaast heb ik ook geleerd hoe ik mijn bronnen in APA-stijl moet noteren.</p>
            <h1>Feedback</h1>
