            <h1>Ervaring</h1>
            <p>We begonnen met een brainstorm. Wat zouden we mogelijk allemaal kunnen maken voor NORE? We hadden een lijst opgesteld en besloten de leukste dingen daaruit te halen. Daarna besloten we een meeting te plannen met Tessa, aangezien zij ons eerste aanspreekpunt was. <br><br> We hebben een lijst met vragen opgesteld, zodat we de band en hun aesthetic beter konden leren kennen. We hebben Tessa ook 23plusone laten doen, om te kijken welk gevoel we met onze producten moesten gaan overbrengen. Dit lijkt een beetje op de "Card sorting"-methode uit de "CMD methods pack"; we hebben een aantal kaarten met begrippen voor Tessa's neus gelegd, en zij moest kiezen welke begrippen het beste bij de band zouden passen. <br><br> Nadat Tessa ons wat verteld had over de band, besloten we onze ideeën met haar te delen. Denk hierbij aan het maken van een website en een Instagram filter. Daarnaast hadden we ook nog het idee om stickers, visitekaartjes en/of buttons te ontwerpen. Ze was erg positief over al onze ideeën, en we kregen haar toestemming om het verder voor haar uit te werken. <br><br> We hadden haar ook nog gevraagd om een moodboard te maken, en om wat foto’s te sturen van websites die zij mooi vindt. Op basis van wat ze ons gestuurd had, besloten we zelf een moodboard (CMD method) te maken om zo de stijl te bepalen waarin we onze producten mogelijk konden maken. Daarna hebben we een stylesheet gemaakt voor onze mediaverkenning, en zijn we allemaal individueel aan de slag gegaan. Iedereen was verantwoordelijk voor zijn eigen onderdeel. Mijn onderdelen waren de cover, de “ontmoet de artiest”-pagina en alles over de website (de website layout, het foto album en de random art knop). Uiteindelijk hebben we op vrijdag 24 september onze mediaverkenning aan haar gepresenteerd, en ze was heel erg tevreden. Ze wilde heel graag een fotoshoot met ons doen, en daarnaast was zo ook erg positief over het idee van een foto-album op haar website. De volgende stap voor ons project was om ontwerpen te gaan maken voor haar producten, en een verdere stijl te gaan bepalen door middel van een brandbook.</p>
